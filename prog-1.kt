fun main() {
    var i: Int
    var j: Int
    var k: Int

    
    for (i in 1..9 step 2) {
        for (k in 1..(9 - i) / 2) {
            print(" ")
        }
        for (j in 1..i) {
            print("*")
        }
        println()
    }

    
    for (i in 9 downTo 1 step 2) {
        for (k in 1..(9 - i) / 2) {
            print(" ")
        }
        for (j in 1..i) {
            print("*")
        }
        println()
    }
}
